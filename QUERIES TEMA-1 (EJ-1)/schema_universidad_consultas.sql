-- Retorna un llistat amb tots els departaments que no han impartit assignatures en cap curs escolar.;
SELECT DISTINCT departamento.nombre
FROM departamento
LEFT JOIN profesor ON departamento.id = profesor.id_departamento
LEFT JOIN asignatura ON profesor.id_profesor = asignatura.id_profesor
LEFT JOIN alumno_se_matricula_asignatura ON asignatura.id = alumno_se_matricula_asignatura.id_asignatura
WHERE alumno_se_matricula_asignatura.id_curso_escolar is null;
-- RESUMEN
-- 1 Retorna el nombre total d'alumnes que hi ha.;
SELECT COUNT(*) FROM persona WHERE tipo = 'alumno';
-- 2 Calcula quants/es alumnes van néixer en 1999.;
SELECT COUNT(*) FROM persona WHERE tipo = 'alumno' AND YEAR(fecha_nacimiento) = '1999';
-- 3 Calcula quants/es professors/es hi ha en cada departament. El resultat només ha de mostrar dues columnes, una amb el nom del departament i una altra amb el nombre de professors/es que hi ha en aquest departament. 
-- El resultat només ha d'incloure els departaments que tenen professors/es associats i haurà d'estar ordenat de major a menor pel nombre de professors/es.;
SELECT departamento.nombre AS departamento, COUNT(*) AS profesores FROM departamento
JOIN profesor ON departamento.id = profesor.id_departamento
GROUP BY departamento ORDER BY profesores DESC;
-- 4 Retorna un llistat amb tots els departaments i el nombre de professors/es que hi ha en cadascun d'ells. Té en compte que poden existir departaments que no tenen professors/es associats/des.
-- Aquests departaments també han d'aparèixer en el llistat.;
SELECT DISTINCT departamento.nombre, persona.nombre
FROM departamento
LEFT JOIN profesor ON departamento.id = profesor.id_departamento
LEFT JOIN persona ON profesor.id_profesor = persona.id;
-- 5 Retorna un llistat amb el nom de tots els graus existents en la base de dades i el nombre d'assignatures que té cadascun. Té en compte que poden existir graus que no tenen assignatures associades. 
-- Aquests graus també han d'aparèixer en el llistat. El resultat haurà d'estar ordenat de major a menor pel nombre d'assignatures.;
SELECT grado.nombre AS Grado, COUNT(*) AS Numero_asignaturas FROM grado
LEFT JOIN asignatura ON grado.id = asignatura.id_grado
GROUP BY grado ORDER BY Numero_asignaturas DESC;
-- 6 Retorna un llistat amb el nom de tots els graus existents en la base de dades i el nombre d'assignatures que té cadascun, dels graus que tinguin més de 40 assignatures associades.;
SELECT grado.nombre AS grado, COUNT(*) AS Numero_asignaturas 
FROM grado
JOIN asignatura ON grado.id = asignatura.id_grado
GROUP BY grado.nombre 
HAVING COUNT(*) >50
ORDER BY Numero_asignaturas;
-- 7 Retorna un llistat que mostri el nom dels graus i la suma del nombre total de crèdits que hi ha per a cada tipus d'assignatura. 
-- El resultat ha de tenir tres columnes: nom del grau, tipus d'assignatura i la suma dels crèdits de totes les assignatures que hi ha d'aquest tipus.;
SELECT asi.nombre 'Asignatura', ce.anyo_inicio 'Año inicio' , ce.anyo_fin 'Año fin' 
FROM alumno_se_matricula_asignatura asma JOIN curso_escolar ce ON ce.id = asma.id_curso_escolar 
JOIN persona pe ON pe.id = asma.id_alumno JOIN asignatura asi ON asi.id = asma.id_asignatura WHERE pe.nif ='26902806M';
-- 8 Retorna un llistat amb el nom de tots els departaments que tenen professors/es que imparteixen alguna assignatura en el Grau en Enginyeria Informàtica (Pla 2015).
SELECT DISTINCT departamento.nombre FROM departamento 
JOIN profesor ON departamento.id = profesor.id_departamento JOIN asignatura ON profesor.id_profesor = asignatura.id_profesor 
JOIN grado ON asignatura.id_grado = grado.id WHERE grado.nombre = 'Grado en Ingeniería Informática (Plan 2015)';
-- 9 Retorna un llistat amb tots els/les alumnes que s'han matriculat en alguna assignatura durant el curs escolar 2018/2019.
SELECT DISTINCT CONCAT(pe.apellido1, ' ', pe.apellido2, ', ', pe.nombre) AS 'Alumnos matriculados 2018/2019' 
FROM persona pe JOIN alumno_se_matricula_asignatura asma ON pe.id = asma.id_alumno 
JOIN curso_escolar ce ON asma.id_curso_escolar = ce.id WHERE ce.anyo_inicio BETWEEN 2018 AND 2019;
